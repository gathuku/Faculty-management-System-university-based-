    private void jComboBox3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox3ActionPerformed

        String value = jComboBox1.getSelectedItem().toString();

        String value2 = jComboBox1.getSelectedItem().toString();
        DefaultTableModel model = new DefaultTableModel();
        model = (DefaultTableModel) jTable1.getModel();
        try {
            String sql = "SELECT firstName,otherNames,regNo,gender FROM students WHERE department='" + value + "' ";//&& course='"+value2+"'";
            st = conn.createStatement();
            rs = st.executeQuery(sql);
            model.setRowCount(0);
            int number = 0;
            if (!rs.next()) {
                model.addRow(new Object[]{"", "", "NO DATA FOUND"});
            } else {
                model.setRowCount(0);
                while (rs.next()) {
                    number++;
                    String fname = rs.getString("firstName");
                    String sname = rs.getString("otherNames");

                    String regno = rs.getString("regNo");
                    String gender = rs.getString("gender");
                    String names = fname + "" + sname;

                    model.addRow(new Object[]{regno, names, gender});
                }
            }
            jLabel4.setText(value2);
            jLabel5.setText(value2);
            jLabel6.setText(String.valueOf(number));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jComboBox3ActionPerformed

