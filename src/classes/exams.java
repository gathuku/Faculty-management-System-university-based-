    public void save_marks() {
        // JOptionPane.showMessageDialog(null, "Hallo there");

        String course = jComboBox2.getSelectedItem().toString();
        String group = jComboBox3.getSelectedItem().toString();
        String year = jComboBox4.getSelectedItem().toString();
        String semester = jComboBox6.getSelectedItem().toString();
        String unit = jComboBox7.getSelectedItem().toString();

        //getting values on the table
        String regNo = jTable1.getValueAt(jTable1.getSelectedRow(), 0).toString();
        String marks = jTable1.getValueAt(jTable1.getSelectedRow(), 3).toString();
        int mark = Integer.valueOf(marks);

        String grade = "-";
        String comment = "";
        if (mark >= 70 && 100 >= mark) {
            grade = "A";
            comment = "Excellent";
        } else if (mark >= 60 && mark < 70) {
            grade = "B";
            comment = "Very good";
        } else if (mark >= 50 && mark < 60) {
            grade = "C";
            comment = "Good";
        } else if (mark >= 40 && mark < 50) {
            grade = "D";
            comment = "Fair";
        } else {
            grade = "F";
            comment = "poor";
        }

        jTable1.setValueAt(grade, jTable1.getSelectedRow(), 4);
        jTable1.setValueAt(comment, jTable1.getSelectedRow(), 5);

        try {
            String sql1 = "INSERT INTO exams(course,regNo,_year,_group,semester,unit,marks,grade,comm)VALUES"
                    + "('" + course + "','" + regNo + "','" + year + "','" + group + "','" + semester + "','" + unit + "','" + marks + "','" + grade + "','" + comment + "')";

            Statement st = conn.createStatement();
            st.executeUpdate(sql1);
//            pst = conn.prepareStatement(sql1);
//            pst.execute();
            //JOptionPane.showMessageDialog(this, "INSERTED");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex);
        }

    }

